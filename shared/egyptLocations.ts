// Helper: Get all governorates
export function getGovernorates(): string[] {
  // Remove duplicate governorates and empty centers
  const seen = new Set<string>();
  return egyptLocations
    .filter(loc => {
      if (seen.has(loc.governorate)) return false;
      seen.add(loc.governorate);
      return loc.centers.length > 0;
    })
    .map(loc => loc.governorate);
}

// Helper: Get centers/cities for a governorate
export function getCenters(governorate: string): string[] {
  const loc = egyptLocations.find(l => l.governorate === governorate && l.centers.length > 0);
  return loc ? loc.centers.map(c => c.name) : [];
}

// Helper: Get villages for a governorate + center
export function getVillages(governorate: string, center: string): string[] {
  const loc = egyptLocations.find(l => l.governorate === governorate && l.centers.length > 0);
  if (!loc) return [];
  const c = loc.centers.find(c => c.name === center);
  return c ? c.villages : [];
}

// Helper: Search all locations (for autocomplete)
export function searchLocations(query: string): { governorate: string; center: string; village: string }[] {
  if (!query || query.length < 2) return [];
  const results: { governorate: string; center: string; village: string }[] = [];
  const q = query.toLowerCase();
  const seen = new Set<string>();

  for (const loc of egyptLocations) {
    if (loc.centers.length === 0) continue;
    for (const center of loc.centers) {
      for (const village of center.villages) {
        if (
          village.includes(q) ||
          center.name.includes(q) ||
          loc.governorate.includes(q)
        ) {
          const key = `${loc.governorate}-${center.name}-${village}`;
          if (!seen.has(key)) {
            seen.add(key);
            results.push({
              governorate: loc.governorate,
              center: center.name,
              village,
            });
          }
        }
      }
      // Also match if center name matches but show first village
      if (center.name.includes(q) && center.villages.length > 0) {
        const key = `${loc.governorate}-${center.name}`;
        if (!seen.has(key)) {
          seen.add(key);
        }
      }
    }
  }

  return results.slice(0, 50);
}
